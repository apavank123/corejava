class Galaxy{
	static String galaxyName;
	static String type;
	static String color;
	static String distance;
	
	static void setGalaxyName(String name){
		galaxyName=name;
	}
	static String getGalaxyName(){
		return galaxyName;
	}
	static void setGalaxyType(String typ){
		type=typ;
	}
	static String getGalaxyType(){
		return type;
	}
	static void setGalaxycolor(String clr){
		color = clr;
	}
	static String getGalaxyColor(){
		return color;
	}
	static void setGalaxyDistance(String dist){
		distance=dist;
	}
	static String getGalaxyDistance(){
		return distance;
	}
}