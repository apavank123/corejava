class Toys{
	static String toysName;
	static String color;
	static int toysPrice;
	
	// setter and getter
	
	static void setToysName(String name){
		toysName=name;
	}
	static String getToysName(){
		return toysName;
	}
	static void setToysColor(String clr){
		color=clr;
	}
	static String getToysColor(){
		return color;
	}
	static void setToysPrice(int price){
		toysPrice=price;
	}
	static int getToysPrice(){
		return toysPrice;
	}
}