class TeaStall{
	
	static int teaStallId;
	static String teaStallName;
	static String address;
	static long contactNo;
	
	//setter and getter
	
	static void setTeaStallId(int Id){
		teaStallId=Id;
	}
	static int getTeaStallId(){
		return teaStallId;
	}
	static void setContactNo(long cNo){
		contactNo = cNo;
	}
	static long getContactNo(){
		return contactNo;
	}
	static void setTeaStallName(String name){
		teaStallName =name;
	}
	static String getTeaStallName(){
		return teaStallName;
	}
	static void setAddress(String deepak){
		address=deepak;
	}
	static String getAddress(){
		return address;
	}
}