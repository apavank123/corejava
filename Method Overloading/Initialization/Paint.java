class Paint{
	
	static int paintId;
	static String brand;
	static String color;
	
	//setter and getter
	
	static void setPaintId(int id){
		paintId=id;
	}
	static int getPaintId(){
		return paintId;
	}
	static void setColor(String clr){
		color = clr;
	}
	static String getColor(){
		return color;
	}
	static void setBrandName(String name){
		brand =name;
	}
	static String getBrandName(){
		return brand;
	}
	
}